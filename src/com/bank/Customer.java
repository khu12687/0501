package com.bank;
class  Customer{
    private String name;
    private int age;
    private boolean isMarry;

	public String getName(){
		return name;
	}

	public String setName(String name){
		this.name = name;
	}

	public int getAge(){
		return age;
	}

	public int setAge(int age){
		this.age = age;
	}

	public boolean getIsMarry(){
		return isMarry;
	}

	public boolean setIsMarry(boolean isMarry){
		this.isMarry = isMarry;
	}
	
}

